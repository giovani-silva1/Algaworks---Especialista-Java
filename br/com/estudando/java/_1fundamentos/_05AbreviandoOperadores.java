package br.com.estudando.java._1fundamentos;

public class _05AbreviandoOperadores {

    public static void main(String[] args) {

        int quantidadeProdutos = 0;
        // forma sem abreviação
         quantidadeProdutos = quantidadeProdutos + 3;

         // forma com abreviação
         quantidadeProdutos +=3;

         quantidadeProdutos -=3;
         quantidadeProdutos*=3;
         quantidadeProdutos /=3;
         quantidadeProdutos %=3;


    }
}
