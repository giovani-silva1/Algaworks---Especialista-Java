package br.com.estudando.java._1fundamentos;

public class _04OperadoresAritmeticos {
    public static void main(String[] args) {

        //operador soma
        int idade = 25;
        int idade2 = 40;
        System.out.println("SOMA:" + (idade + idade2));

        //operador subtração
        int idade3 = 25;
        int idade4 = 40;

        System.out.println("SUBTRAÇÃO:" + (idade - idade2));

        //operador multiplicacao
        int idade5 = 25;
        int idade6 = 40;

        System.out.println("MULTIPLICAÇÃO:" + (idade * idade2));


        //operador divisão

        int idade7 = 25;
        int idade8 = 40;

        System.out.println("DIVISÃO:" + (idade / idade2));

        //operador MÓDULO

        int idade9 = 25;
        int idade10 = 40;

        System.out.println("DIVISÃO:" + (idade9 % idade10));
    }
}
